# Program that calculates the area of ​​the rhombus
# 	A = ½ × d1 × d2

first_diagonal = float(input("Enter the first diagonal: "))
second_diagonal = float(input("Enter the second diagonal: "))

area_of_rhombus = (1/2) * first_diagonal * second_diagonal

print("Area of rhombus is:", area_of_rhombus)